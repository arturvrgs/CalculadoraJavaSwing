    private void btIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btIgualActionPerformed
        campo_historico.setText("");    
        n2 = Float.parseFloat(campo_resultado.getText()); 
        switch (escolha) {
            case "%":
                  campo_resultado.setText(Float.toString(n2 * (n1/100))); 
                break;
            case "+": 
                  campo_resultado.setText(Float.toString(n1+n2));
                break;
            case "-":
                campo_resultado.setText(Float.toString(n1-n2));
                break;
            case "/":
                campo_resultado.setText(Float.toString(n1/n2));
                break;
            case "x":
                campo_resultado.setText(Float.toString(n1*n2));
                break;
        }
         
    }//GEN-LAST:event_btIgualActionPerformed

